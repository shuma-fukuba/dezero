from core import Function


class Pow(Function):
    def __init__(self, c) -> None:
        super().__init__()
        self.c = c

    def forward(self, x):
        y = x ** self.c
        return y

    def backward(self, gy):
        x = self.inputs[0].data
        c = self.c
        gx = c * x ** (c - 1) * gy
        return gx
